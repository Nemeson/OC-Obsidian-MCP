#!/usr/bin/env node
/**
 * ObMem v2.5: Goal Tracker Command (/goal)
 *
 * Usage:
 *   obmem goal
 *   obmem goal "Implement auth flow" --project PCAP2KML
 *   obmem goal "Fix memory leak" --status done
 *   obmem goal --list
 *   obmem goal --project PCAP2KML --list
 *
 * Manages goals in:
 *   OpenCode/Goals/{project}.md
 */

const fs = require('fs');
const path = require('path');
const os = require('os');

// ─── Load .mcp-env ──────────────────────────────────────
function loadMcpEnv() {
  const candidates = [
    path.join(__dirname, '..', 'config', '.mcp-env'),
    path.join(os.homedir(), '.oc-obsidian-mcp', 'config', '.mcp-env'),
  ];
  for (const f of candidates) {
    if (fs.existsSync(f)) {
      for (const line of fs.readFileSync(f, 'utf8').split(/\r?\n/)) {
        const t = line.trim();
        if (!t || t.startsWith('#')) {continue;}
        const eq = t.indexOf('=');
        if (eq === -1) {continue;}
        const key = t.substring(0, eq).trim();
        const val = t.substring(eq + 1).trim();
        if (!process.env[key]) {process.env[key] = val;}
      }
      break;
    }
  }
}
loadMcpEnv();

const VAULT_PATH = process.env.OBSIDIAN_VAULT_PATH || '';
const GOALS_FOLDER = process.env.GOALS_FOLDER || 'OpenCode/Goals';

// ─── Helpers ────────────────────────────────────────────
function getDate() {
  const n = new Date();
  return `${n.getFullYear()}-${String(n.getMonth()+1).padStart(2,'0')}-${String(n.getDate()).padStart(2,'0')}`;
}

function detectProject() {
  try {
    const { execSync } = require('child_process');
    const top = execSync('git rev-parse --show-toplevel', { encoding:'utf8', timeout:3000, stdio:['pipe','pipe','ignore'] }).trim();
    return path.basename(top);
  } catch { return '_global'; }
}

function parseArgs() {
  const args = process.argv.slice(2);
  const opts = { title: null, project: null, status: 'active', list: false, dryRun: false };
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a === '--project') {opts.project = args[++i];}
    else if (a === '--status') {opts.status = args[++i];}
    else if (a === '--list') {opts.list = true;}
    else if (a === '--dry-run') {opts.dryRun = true;}
    else if (!a.startsWith('-') && !opts.title) {opts.title = a;}
  }
  return opts;
}

function parseGoals(content) {
  const goals = [];
  const lines = content.split(/\r?\n/);
  let inGoals = false;
  for (const line of lines) {
    const trimmed = line.trim();
    if (trimmed.match(/^##?\s*(Goals|Ziele)/i)) { inGoals = true; continue; }
    if (inGoals && trimmed.match(/^##/)) {break;}
    if (inGoals && trimmed.match(/^(-|\*)\s*\[([ x])\]\s*(.+)/)) {
      const done = RegExp.$2 === 'x';
      const title = RegExp.$3.trim();
      goals.push({ done, title });
    }
  }
  return goals;
}

function generateGoalsFile(project, goals) {
  const date = getDate();
  let body = `---
type: goals
project: ${project}
updated: ${date}
---

# 🎯 Goals: ${project}

## Active Goals

`;
  const active = goals.filter(g => !g.done);
  const done = goals.filter(g => g.done);

  if (active.length) {
    for (const g of active) {body += `- [ ] ${g.title}\n`;}
  } else {
    body += '_No active goals. Add one with `obmem goal "Title"`_\n';
  }

  body += '\n## Completed Goals\n\n';
  if (done.length) {
    for (const g of done) {body += `- [x] ${g.title}\n`;}
  } else {
    body += '_No completed goals yet._\n';
  }

  body += '\n---\n_Generated by ObMem Goal Tracker v2.5_\n';
  return body;
}

function loadGoals(project) {
  const file = path.join(VAULT_PATH, GOALS_FOLDER, `${project}.md`);
  if (!fs.existsSync(file)) {return [];}
  return parseGoals(fs.readFileSync(file, 'utf8'));
}

function saveGoals(project, goals) {
  const dir = path.join(VAULT_PATH, GOALS_FOLDER);
  if (!fs.existsSync(dir)) {fs.mkdirSync(dir, { recursive: true });}
  const file = path.join(dir, `${project}.md`);
  fs.writeFileSync(file, generateGoalsFile(project, goals), 'utf8');
  return file;
}

// ─── Main ───────────────────────────────────────────────
function main() {
  if (!VAULT_PATH) {
    console.error('Error: OBSIDIAN_VAULT_PATH not set. Run setup first: obmem setup');
    process.exit(1);
  }

  const opts = parseArgs();
  const project = opts.project || detectProject();
  const goalsFile = path.join(VAULT_PATH, GOALS_FOLDER, `${project}.md`);

  if (opts.list) {
    console.log(`\n🎯 Goals for ${project}:\n`);
    if (!fs.existsSync(goalsFile)) {
      console.log('   No goals file found. Create one with `obmem goal "Title"`\n');
      process.exit(0);
    }
    const goals = loadGoals(project);
    const active = goals.filter(g => !g.done);
    const done = goals.filter(g => g.done);
    console.log(`   Active (${active.length}):`);
    for (const g of active) {console.log(`   - [ ] ${g.title}`);}
    console.log(`\n   Completed (${done.length}):`);
    for (const g of done) {console.log(`   - [x] ${g.title}`);}
    console.log();
    return;
  }

  if (!opts.title) {
    console.error('Error: Title required. Usage: obmem goal "Title" [--project P] [--status done]');
    process.exit(1);
  }

  const goals = loadGoals(project);
  const existing = goals.find(g => g.title.toLowerCase() === opts.title.toLowerCase());
  const done = opts.status === 'done' || opts.status === 'completed';

  if (existing) {
    existing.done = done;
    const action = done ? 'marked as completed' : 'reactivated';
    console.log(`\n🔄 Goal "${opts.title}" ${action} for ${project}`);
  } else {
    goals.push({ title: opts.title, done });
    const action = done ? 'added and completed' : 'added';
    console.log(`\n📝 Goal "${opts.title}" ${action} for ${project}`);
  }

  if (opts.dryRun) {
    console.log('\n--- DRY-RUN: Would write ---\n');
    console.log(generateGoalsFile(project, goals));
    console.log('\n--- End dry-run ---\n');
    return;
  }

  const outFile = saveGoals(project, goals);
  console.log(`   💾 Saved to ${outFile.replace(VAULT_PATH + path.sep, '')}`);
  console.log(`\n🎯 Goal tracking updated for ${project}\n`);
}

main();
